import { readFileSync, writeFileSync } from "node:fs"

const sceneSource = readFileSync("components/globe/globe-scene.tsx", "utf8")
const densityMatch = sceneSource.match(/density:\s*(\d+)/)
const rows = densityMatch ? Number(densityMatch[1]) : 160

const topoData = JSON.parse(
  readFileSync("node_modules/world-atlas/land-110m.json", "utf8")
)

// Minimal TopoJSON to GeoJSON conversion for MultiPolygon
function arcToCoords(arc, transform) {
  const coords = []
  let x = 0
  let y = 0
  for (const [dx, dy] of arc) {
    x += dx
    y += dy
    coords.push([
      x * transform.scale[0] + transform.translate[0],
      y * transform.scale[1] + transform.translate[1],
    ])
  }
  return coords
}

function decodeArc(arcIdx, arcs, transform) {
  const reverse = arcIdx < 0
  const idx = reverse ? -1 - arcIdx : arcIdx
  const coords = arcToCoords(arcs[idx], transform)
  if (reverse) {
    coords.reverse()
  }
  return coords
}

function decodeRing(ring, arcs, transform) {
  const coords = []
  for (const arcIdx of ring) {
    const arcCoords = decodeArc(arcIdx, arcs, transform)
    const start = coords.length === 0 ? 0 : 1
    for (let i = start; i < arcCoords.length; i += 1) {
      coords.push(arcCoords[i])
    }
  }
  return coords
}

function collectGeomPolygons(geom, arcs, transform, out) {
  if (geom.type === "Polygon") {
    out.push(geom.arcs.map((r) => decodeRing(r, arcs, transform)))
  } else if (geom.type === "MultiPolygon") {
    for (const poly of geom.arcs) {
      out.push(poly.map((r) => decodeRing(r, arcs, transform)))
    }
  }
}

function topoToGeo(topo) {
  const { arcs, transform } = topo
  const obj = topo.objects.land
  if (obj.type !== "GeometryCollection") {
    return []
  }
  const polygons = []
  for (const geom of obj.geometries) {
    collectGeomPolygons(geom, arcs, transform, polygons)
  }
  return polygons
}

// Ray-casting point-in-polygon test
function pointInPolygon(lng, lat, ring) {
  let inside = false
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i, i += 1) {
    const [xi, yi] = ring[i]
    const [xj, yj] = ring[j]
    if (
      yi > lat !== yj > lat &&
      lng < ((xj - xi) * (lat - yi)) / (yj - yi) + xi
    ) {
      inside = !inside
    }
  }
  return inside
}

function isInsidePolygon(polygon, lng, lat) {
  const [outer] = polygon
  if (!pointInPolygon(lng, lat, outer)) {
    return false
  }
  for (let h = 1; h < polygon.length; h += 1) {
    if (pointInPolygon(lng, lat, polygon[h])) {
      return false
    }
  }
  return true
}

function isLand(polygons, lng, lat) {
  return polygons.some((p) => isInsidePolygon(p, lng, lat))
}

const polygons = topoToGeo(topoData)

const step = 180 / rows
const landDots = []

for (let lat = -90; lat <= 90; lat += step) {
  const cos = Math.cos((lat * Math.PI) / 180)
  const cols = Math.max(1, Math.round(rows * 2 * cos))
  for (let i = 0; i < cols; i += 1) {
    const lng = -180 + (360 / cols) * i
    if (isLand(polygons, lng, lat)) {
      landDots.push([
        Math.round(lat * 1000) / 1000,
        Math.round(lng * 1000) / 1000,
      ])
    }
  }
}

const output = `// Auto-generated by scripts/generate-land-dots.mjs
// ${landDots.length} land dot positions [lat, lng]
export const landDots: [number, number][] = ${JSON.stringify(landDots)}
`

writeFileSync("data/land-dots.ts", output)
console.log(`Generated ${landDots.length} land dots → data/land-dots.ts`)
